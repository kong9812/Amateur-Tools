#include "main.h"
#include "basic.h"
#include "input.h"
#include "waveBackground.h"
#include "waveControl.h"
#include "waveManager.h"

//*****************************************************************************
// プロトタイプ宣言
//*****************************************************************************


//*****************************************************************************
// グローバル変数:
//*****************************************************************************


//*****************************************************************************
// 初期化処理
//*****************************************************************************
HRESULT InitWaveManager(int InitType)
{
	// 波の背景
	InitWaveBackground(InitType);

	// 波のコントロール
	InitWaveControl(InitType);

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitWaveManager(void)
{
	// 波の背景
	UninitWaveBackground();

	// 波のコントロール
	UninitWaveControl();
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateWaveManager(HWND hWnd)
{
	// 波の背景
	UpdateWaveBackground();

	// 波のコントロール
	UpdateWaveControl(hWnd);
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawWaveManager(void)
{
	// 波の背景
	DrawWaveBackground();

	// 波のコントロール
	DrawWaveControl();
}